CREATE STREAM PRODUCT_RAW (
  OP VARCHAR,
  AFTER STRUCT<
    ID INT,
    NAME VARCHAR,
    SLUG VARCHAR,
    SHORT_DESCRIPTION VARCHAR,
    FULL_DESCRIPTION VARCHAR,
    CREATED_AT BIGINT,
    UPDATED_AT BIGINT,
    SELLER_ID INT,
    BRAND_ID INT,
    STATUS VARCHAR
  >
) WITH (
  KAFKA_TOPIC = 'products-events',
  VALUE_FORMAT = 'JSON'
);


CREATE STREAM BRAND_RAW (
  OP VARCHAR,
  AFTER STRUCT<
    ID INT,
    NAME STRING,
    SLUG STRING,
    DESCRIPTION STRING,
    LOGO_URL STRING,
    WEBSITE STRING
  >
) WITH (
  KAFKA_TOPIC = 'brands-events',
  VALUE_FORMAT = 'JSON'
);


CREATE STREAM SELLER_RAW (
  OP VARCHAR,
  AFTER STRUCT<
    ID INT,
    NAME STRING,
    DESCRIPTION STRING,
    LOGO_URL STRING,
    RATING DOUBLE,
    CONTACT_EMAIL STRING,
    CONTACT_PHONE STRING,
    CREATED_AT STRING
  >
) WITH (
  KAFKA_TOPIC = 'sellers-events',
  VALUE_FORMAT = 'JSON'
);
